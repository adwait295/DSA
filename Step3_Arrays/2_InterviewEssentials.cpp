/*
Whenever we go for an interview, the interviewer gives us a question. 

If we know the solution of the question, we should not tell the solution directly.

We might know the solution right away but we should not tell it.
We should build the interview. Its us who should drive the interview. 

Our thought process should be visible to the interviewer.

There should be step by step optimization of approaches.
Because it leaves a very good impact on the interviewer.

Driving the interview:
Imagine we are asked a hard question and we know the most optimized solution in terms
of T.C and S.C.

But we shouldn't say it directly.

At first we should ask the interviewer about the test cases. 
Then once we have understood the question properly, we should give him the 
Brute Force solution which is the most normal solution which comes to our brain at first.
For every problem, brute force solution usually does exist.

While writing any solution, we should always tell the interviewer about the edge cases
and how we deal with them.

Then we go ahead and optimize the brute and give a better solution.
For every problem, better might or might not exist.

Then we optimize the better to get the most Optimal solution.

There can be other flows as well. e.g. :
Brute -> Better -> More better -> Optimal 

Its us who will drive the interview.
Its us who will decide how that 30 mins or 1 hour is driven. 

So if we know the solution and say it in the first 2 minutes, it might be an issue.

So always drive the interview. Show that we can think right from the scratch, we 
can build it. Sometimes, we have to fake it but that is how interviews go in DSA rounds.

In harder problems we have to follow this way. In easy problems, don't follow it.

*/